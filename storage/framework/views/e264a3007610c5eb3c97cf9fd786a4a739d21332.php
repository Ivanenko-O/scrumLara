                                Команды могут самоорганизоваться, но для этого требуются наделить их полномочиями, авторизировать и повысить уровень доверия со стороны руководства.</p>
                            <p><b style="color: #1ABC9C">Вы узнаете,</b> как достичь самоорганизации, как распределять полномочия в организации, какие могут быть проблемы расширения прав и возможностей, как развивать доверительные отношения, а также несколько методов распределенного управления.</p>
